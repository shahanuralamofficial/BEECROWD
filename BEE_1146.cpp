#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a;
    while (1)
    {
        cin >> a;
        for (int i = 1; i <= a; i++)
        {
            cout <<i<<" ";
        }
        cout<<endl;
        if (a == 0)
        {
            break;
        }
    }
    return 0;
}